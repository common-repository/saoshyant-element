     $.fn.sor_fold_item = function() { 

		$(this).find('.sor_options').find('.sor_options_fold').each(function() {
			var show;
			$(this).find('.sor_options_fold_item').each(function() {
 				var data_name = $(this).attr('data-name'); 			
				var data_value = $(this).attr('data-value');
				var type = $(this).parents('.sor_options').find('[name="'+data_name+'"]').attr('type');
				if(type === 'radio'){
   					if( $(this).parents('.sor_options').find('[name="'+data_name+'"][value="'+data_value+'"]').is(':checked')){
						show = 'checked';
 					}
 				}else if(type === 'checkbox'){
  					if($(this).parents('.sor_options').find('[name="'+data_name+'"]').is(':checked')){
						show = 'checked';
					}
 				}else{
					if(data_name ==='icon'){
						var icon =$(this).parents('.sor_options').find('[name="icon"]').val();
						if(isset(icon)){
  							show = 'checked';
						}
					}else{
 						var val =$(this).parents('.sor_options').find('[name="'+data_name+'"]').val();
						if(data_value === val){
							show = 'checked';
						}
					}
 				}
 				var actives  = $(this).parents('.sor_options').find('[name="'+data_name+'"]').parents('.sor_options_item').attr('data-active');
				if(	 actives === 'hide' ){
					show = '';
				}
			});
			
			if( show === 'checked' ){
				$(this).parent().attr('data-active','show');
			}else{
				$(this).parent().attr('data-active','hide');
			}
 		});	
		if( $(this).find('[name="sor_setting[type]"][value="slider"]').is(':checked')){
   			$('.sor_module_setting').removeClass('sor_module_type_slider sor_module_type_glider sor_module_type_single');
  			$('.sor_module_setting').addClass('sor_module_type_slider');
		 }
		if( $(this).find('[name="sor_setting[type]"][value="single"]').is(':checked')){
   			$('.sor_module_setting').removeClass('sor_module_type_slider sor_module_type_glider sor_module_type_single');
  			$('.sor_module_setting').addClass('sor_module_type_single');
		 }
		if( $(this).find('[name="sor_setting[type]"][value="glider"]').is(':checked')){
   			$('.sor_module_setting').removeClass('sor_module_type_slider sor_module_type_glider sor_module_type_single');
  			$('.sor_module_setting').addClass('sor_module_type_glider');
		 }
 
	};
