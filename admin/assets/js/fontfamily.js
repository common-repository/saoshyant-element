(function ($, undefined) {
	  'use strict';
function isset(iVal){
	 return (iVal!=="" && iVal!=null && iVal!==undefined && typeof(iVal) != "undefined") ? 1 : 0;
} 
 
/*************************************************************************************************************************************************************************
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 
 																		size %
 
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
**************************************************************************************************************************************************************************/	
$.fn.sor_input_fontfamily = function(id) { 
		 
		 
 	 var fontfamily = $(this).find('.sor_live_'+id+'_fontfamily select').val() ;
	 var fontweight = $(this).find('.sor_live_'+id+'_font_weight select').val() ;
  
		 
  	var width='400';
  	if(isset(fontfamily) ){
		/*************************************************************************************************************************************************************************
		--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 																				Iransans
		 --------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		**************************************************************************************************************************************************************************/	
		if(fontfamily ==='sor-iransans' ){
			if(fontweight==='300'){
				width='300';
				
			}else if(fontweight==='normal'||fontweight==='400'){
				width='400';
				
			}else if(fontweight==='500'||fontweight==='600'){
				width='500';
				
			}else if(fontweight==='bold' || fontweight==='700' ){
				width='700';
				
			}else if(fontweight==='800' || fontweight==='900' ){
				width='900';
				
			}else{
				width='400';
			}
		/*************************************************************************************************************************************************************************
		--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 																				Vazer
		 --------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		**************************************************************************************************************************************************************************/	
 		}else if(fontfamily ==='sor-vazir' ){
			if(fontweight==='300'){
				width='300';
				
			}else if(fontweight==='normal'||fontweight==='400' || fontweight==='500'){
				width='400';
				
			 	
			}else if(fontweight==='bold' || fontweight==='600' || fontweight==='700' ){
				width='700';
				
			}else if(fontweight==='800' || fontweight==='900' ){
				width='900';
				
			}else{
				width='400';
			}
 			
		 
		/*************************************************************************************************************************************************************************
		--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 																				nahid & segoe
		 --------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		**************************************************************************************************************************************************************************/	
 		}else if(fontfamily ==='sor-nahid' || fontfamily ==='sor-segoe' ){
 				width='400';			 
 			
 		/*************************************************************************************************************************************************************************
		--------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 																				sor-parastoo & sor-sahel & sor-samim & sor-shabnam
		 --------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		**************************************************************************************************************************************************************************/	
 		}else if(fontfamily ==='sor-parastoo' || fontfamily ==='sor-sahel' || fontfamily ==='sor-samim' || fontfamily ==='sor-shabnam' ){
		 	if(fontweight==='normal' || fontweight==='300' ||fontweight==='400' || fontweight==='500' ){
				width='400';
				
			 
			}else if(fontweight==='600' || fontweight==='bold' || fontweight==='700' || fontweight==='800' || fontweight==='900'  ){
				width='700';
				
			}else{
				width='400';
			}			 
 			
		}		
			
		 
		var fontface="@font-face{font-family:'"+fontfamily+"';font-weight:"+width+";src: url('"+sorsorak.font_url+"/"+fontfamily+"-"+width+".woff') format('woff');}";
  		return fontface ;
 	}else{
		return '';
	}

  
};
 })(jQuery);